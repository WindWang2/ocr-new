import sqlite3
import os

db_path = os.path.join("backend", "experiments.db")

def upgrade_d0_prompt_v3():
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    # D0 - 混调器：全量字段布局感知 Prompt
    prompt_d0 = r'''这是超级吴菌混调器的控制屏幕，请执行全量数值提取。

【第一步：模式识别】
观察屏幕左侧边缘垂直排列的五个按钮：
- 如果第一个按钮（(A) 自动）的底色比周围亮（高亮），则输出 "模式": "自动"。
- 如果第二个按钮（手形 手动）的底色比周围亮（高亮），则输出 "模式": "手动"。

【第二步：数值提取】

1. 如果是【自动模式】：
   - 寻找顶部三个并排的方框表格（段一、段二、段三）：
     - 段一：提取 "转速" 下方的数字（如 10000）和 "时间" 下方的数字（如 20）。
     - 段二：提取 "转速" 下方的数字（如 16000）和 "时间" 下方的数字（如 20）。
     - 段三：提取 "转速" 下方的数字（如 22000）和 "时间" 下方的数字（如 20）。
   - 寻找中间的长条区域：
     - 提取 "总共时长(S)" 右侧的数字。
     - 提取 "剩余时长(S)" 右侧的数字。
   - 寻找底部的长条区域：
     - 提取 "当前段数" 右侧的数字。
     - 提取 "当前转速(转)" 右侧的数字。

2. 如果是【手动模式】：
   - 寻找顶部两个并排的方框表格（高速、低速）：
     - 高速：提取 "转速" 和 "时间" 下方的数字。
     - 低速：提取 "转速" 和 "时间" 下方的数字。
   - 寻找底部的长条区域：
     - 提取 "剩余时间(S)" 右侧的数字。
     - 提取 "当前转速(转)" 右侧的数字。

【输出要求】
- 必须严格输出单行 JSON，使用中文键名。
- 严禁输出单位，严禁复读占位符。若图中某项为 ##.## 或看不清，请填 null。

JSON 示例（自动模式）：
{"模式": "自动", "段一转速": "10000", "段一时间": "20", "段二转速": "16000", "段二时间": "20", "段三转速": "22000", "段三时间": "20", "总共时长": "60", "剩余时长": "1", "当前段数": "1", "当前转速": "0"}'''

    cursor.execute('UPDATE instrument_templates SET prompt_template = ? WHERE instrument_type = ?', (prompt_d0, '0'))
    
    conn.commit()
    conn.close()
    print("D0 提示词已升级 v3：强化了区域定位和高亮按钮识别。")

if __name__ == "__main__":
    upgrade_d0_prompt_v3()
